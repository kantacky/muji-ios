import SwiftUI

public extension Color {
    enum MUJI {}
}

public extension Color.MUJI {
    static let beige = Color(.MUJI.beige)
    static let black = Color(.MUJI.black)
    static let blue = Color(.MUJI.blue)
    static let gray = Color(.MUJI.gray)
    static let red = Color(.MUJI.red)
    static let white = Color(.MUJI.white)
}
